package fundamentos.java;

public class BucleFor {

	public static void main(String[] args) {
		for(int i= 1; i <10; i++) {
			 int resultado1 = i; 
		
		for(int j= 1; j <10; j++) {
			int resultado2 = j;
		
		int resultadoFinal = resultado1 * resultado2; 
		System.out.println(resultado1 + "*" + resultado2 + " = " + resultadoFinal);
		}
		
		}
	}
}

